/**
 * Пример
 *
 * Проверка числа на простоту -- результат true, если число простое
 */
fun isPrime(n: Int): Boolean {
    if (n < 2) return false
    for (m in 2..Math.sqrt(n.toDouble()).toInt()) {
        if (n % m == 0) return false
    }
    return true
}

/**
 * Пример
 *
 * Проверка числа на совершенность -- результат true, если число совершенное
 */
fun isPerfect(n: Int): Boolean {
    var sum = 1
    for (m in 2..n/2) {
        if (n % m > 0) continue
        sum += m
        if (sum > n) break
    }
    return sum == n
}

/**
 * Пример
 *
 * Найти число вхождений цифры m в число n
 */
fun digitCountInNumber(n: Int, m: Int): Int =
        when {
            n == m -> 1
            n < 10 -> 0
            else -> digitCountInNumber(n / 10, m) + digitCountInNumber(n % 10, m)
        }

/**
 * Тривиальная
 *
 * Найти количество цифр в заданном числе n.
 * Например, число 1 содержит 1 цифру, 456 -- 3 цифры, 65536 -- 5 цифр.
 */
fun digitNumber(n: Int): Int {
    var digits = 0
    if (n == 0) return 1
    var number = n
    while (number != 0) {
        number /= 10;
        ++digits
    }
    return digits
}

/**
 * Простая
 *
 * Найти число Фибоначчи из ряда 1, 1, 2, 3, 5, 8, 13, 21, ... с номером n.
 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */
fun fib(n: Int): Int {
    var prev = 0
    var cur  = 1
    var number = 1
    while (number < n) {
        val nxt = cur + prev
        prev = cur
        cur = nxt
        ++number
    }
    return cur
}

fun gcd(a: Int, b: Int): Int = if (b == 0) a else gcd(b, a % b)

/**
 * Простая
 *
 * Для заданных чисел m и n найти наименьшее общее кратное, то есть,
 * минимальное число k, которое делится и на m и на n без остатка
 */
fun lcm(m: Int, n: Int) = m / gcd(m, n) * n

/**
 * Простая
 *
 * Для заданного числа n > 1 найти минимальный делитель, превышающий 1
 */
fun minDivisor(n: Int): Int {
    for (i in 2..(Math.sqrt(n.toDouble()).toInt()+1)) {
        if (n % i == 0) return i
    }
    return n
}

/**
 * Простая
 *
 * Для заданного числа n > 1 найти максимальный делитель, меньший n
 */
fun maxDivisor(n: Int): Int = n / minDivisor(n)

/**
 * Простая
 *
 * Определить, являются ли два заданных числа m и n взаимно простыми.
 * Взаимно простые числа не имеют общих делителей, кроме 1.
 * Например, 25 и 49 взаимно простые, а 6 и 8 -- нет.
 */
fun isCoPrime(m: Int, n: Int) = gcd(m, n) == 1

/**
 * Простая
 *
 * Для заданных чисел m и n, m <= n, определить, имеется ли хотя бы один точный квадрат между m и n,
 * то есть, существует ли такое целое k, что m <= k*k <= n.
 * Например, для интервала 21..28 21 <= 5*5 <= 28, а для интервала 51..61 квадрата не существует.
 */
fun squareBetweenExists(m: Int, n: Int): Boolean {
    var l = 0L
    var r = n.toLong() + 1
    while (r - l > 1) {
        val mid = (r + l) / 2L
        if (mid * mid > n) r = mid
        else l = mid
    }
    return l * l in m..n
}

/**
 * Средняя
 *
 * Для заданного x рассчитать с заданной точностью eps
 * sin(x) = x - x^3 / 3! + x^5 / 5! - x^7 / 7! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю
 */
fun sin(x: Double, eps: Double): Double {
    if (x < 0) return -sin(-x, eps)
    val angle = x.rem(2.0 * Math.PI)
    var curSign = 1
    var curPart = angle / 1.0
    var res = 0.0
    var lastFactor = 1.0
    while (curPart >= eps) {
        res += curPart * curSign
        curSign *= -1
        curPart /= ++lastFactor
        curPart /= ++lastFactor
        curPart *= angle * angle
    }
    return res
}

/**
 * Средняя
 *
 * Для заданного x рассчитать с заданной точностью eps
 * cos(x) = 1 - x^2 / 2! + x^4 / 4! - x^6 / 6! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю
 */
fun cos(x: Double, eps: Double): Double {
    val angle = x.rem(2.0 * Math.PI)
    var curSign = -1
    var curPart = angle * angle / 2.0
    var res = 1.0
    var lastFactor = 2.0
    while (curPart >= eps) {
        res += curPart * curSign
        curSign *= -1
        curPart *= angle * angle / (++lastFactor) / (++lastFactor)
    }
    return res
}

/**
 * Средняя
 *
 * Поменять порядок цифр заданного числа n на обратный: 13478 -> 87431.
 * Не использовать строки при решении задачи.
 */
fun revert(n: Int): Int {
    var res = 0
    var number = n
    while (number > 0) {
        res *= 10
        res += number % 10
        number /= 10
    }
    return res
}

/**
 * Средняя
 *
 * Проверить, является ли заданное число n палиндромом:
 * первая цифра равна последней, вторая -- предпоследней и так далее.
 * 15751 -- палиндром, 3653 -- нет.
 */
fun isPalindrome(n: Int) = n == revert(n)

/**
 * Средняя
 *
 * Для заданного числа n определить, содержит ли оно различающиеся цифры.
 * Например, 54 и 323 состоят из разных цифр, а 111 и 0 из одинаковых.
 */
fun hasDifferentDigits(n: Int): Boolean {
    var numberDigit = -1
    var number = n
    while (number > 0) {
        val curDigit = number % 10
        number /= 10
        if (curDigit == numberDigit) continue
        if (numberDigit == -1) numberDigit = curDigit
        else return true
    }
    return false
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из квадратов целых чисел:
 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 */
fun squareSequenceDigit(n: Int): Int {
    var digitsRemained = n - 1
    var curNumber = 1
    while (digitNumber(curNumber * curNumber) <= digitsRemained) {
        digitsRemained -= digitNumber(curNumber * curNumber)
        curNumber++
    }
    return (curNumber * curNumber).toString()[digitsRemained].toInt() - '0'.toInt()
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из чисел Фибоначчи (см. функцию fib выше):
 * 1123581321345589144...
 * Например, 2-я цифра равна 1, 9-я 2, 14-я 5.
 */
fun fibSequenceDigit(n: Int): Int {
    var prev = 0
    var cur  = 1
    var digitsRemained = n - 1
    while (digitNumber(cur) <= digitsRemained) {
        digitsRemained -= digitNumber(cur)
        val nxt = cur + prev
        prev = cur
        cur = nxt
    }
    return cur.toString()[digitsRemained].toInt() - '0'.toInt()
}
